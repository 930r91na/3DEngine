
namespace PLAYGROUND
{
    public class Vertex
    {
        public float X { get; set; }
        public float Y { get; set; }
        public float Z { get; set; }
        public float H { get; set; }


        public Vertex(float x, float y, float z, float h)
        {
            X = x;
            Y = y;
            Z = z;
            H = h;
        }
    }
}
